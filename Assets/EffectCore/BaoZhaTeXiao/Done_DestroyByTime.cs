using UnityEngine;

public class Done_DestroyByTime : MonoBehaviour
{
	[SerializeField] private float time = 1;

	// ------------------------------------------------------------------------------------------------------------------
	// Unity方法
	// ------------------------------------------------------------------------------------------------------------------
	void Update()
	{
		time -= Time.deltaTime;
	    if(time<=0) 
		{
			Destroy (this.gameObject);
		}
	}
}
